package com.huban.construct;

public class AreaRetrunModel {

	private int regionsId;

	private int regionsParentId;

	private String regions;

	private String code;

	public int getRegionsId() {
		return regionsId;
	}

	public void setRegionsId(int regionsId) {
		this.regionsId = regionsId;
	}

	public int getRegionsParentId() {
		return regionsParentId;
	}

	public void setRegionsParentId(int regionsParentId) {
		this.regionsParentId = regionsParentId;
	}

	public String getRegions() {
		return regions;
	}

	public void setRegions(String regions) {
		this.regions = regions;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
